#escape Sequences

#QUOTES
txt="The most hygenic country of the world is \"Singapore\""
print(txt)

#BLACKSLASH
txt="The most hygenic country of the world is \\Singapore"
print(txt)

#NEW LINE
txt="The most hygenic country of the world is \nSingapore"
print(txt)

#CARRAIGE RETURN
txt="The most hygenic country of the world is \rSingapore"
print(txt)

#TAB SPACE
txt="The most hygenic country of the world is \tSingapore"
print(txt)

#BACKSPACE
txt="Thr most hygrnic country of thr world is \bSingapore"
print(txt)

