#Day of the week
def day_of_the_week(day):
    if(day==1):
        print("Sunday")
    elif(day==2):
        print("Monday")
    elif(day==3):
        print("Tuesday")
    elif(day==4):
        print("Wednesday")
    elif(day==5):
        print("Thursday")
    elif(day==6):
        print("Friday")
    elif(day==7):
        print("Saturday")
day_of_the_week(int(input("Enter the no.of the day:")))
    